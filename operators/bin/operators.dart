void main(List<String> args) {
  const myOrange = 20;
  const yourOrange = 10;
  print(myOrange + yourOrange);
  print(myOrange + yourOrange + 20);
  print(myOrange - yourOrange);
  print(myOrange * yourOrange);
  print(myOrange / yourOrange);

  const intDivision = myOrange ~/ yourOrange;
  print(intDivision);
}
